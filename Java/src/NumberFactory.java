class NumberFactory {

    int add(int a, int b) {

        return a + b;

    }

    int subtract(int a, int b) {

        return a - b;

    }

    int multiply(int a, int b) {

        return a * b;

    }

    float divide(int a, int b) {

        return (float) a / (float) b;

    }

    int square(int a) {

        return a * a;

    }

}
